// to print letter C

void main()
{
    int height = 5; 
    int i, j;
    for (i=0; i<height; i++) {
        printf("*");
        for (j=0; j<(height-1); j++) {
            if (i==0 || i==height-1)
                printf("*");
            else
                continue;
        }
        printf("\n");
    }
}


