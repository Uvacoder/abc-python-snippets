#include <stdio.h>

// to print letter A

void main()
{
    int height = 5, width = 9;
    int n = width / 2, i, j;
    for (i=0; i<height; i++) {
        for (j=0; j<=width; j++) {
            if (j==n || j==(width - n) || (i == height / 2 && j > n && j < (width - n))) {
                printf("*");
            } else {
                printf(" ");
            }
        }
        printf("\n");
        n--;
    }
}


